export function isNewArrival(dateAdded: string, daysThreshold: number = 14): boolean {
  const addedDate = new Date(dateAdded).getTime();
  const now = Date.now();
  const diffDays = (now - addedDate) / 86400000;
  return diffDays <= daysThreshold;
}
